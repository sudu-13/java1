public class bitManipulation1 {
    public static void main(String args[]){
        int a=5 , b=6 ;
        // Binary AND(&)
        System.out.println((a&b));
        // Binary OR(|)
        System.out.println((a|b));
        // Binary XOR(^)
        System.out.println((a^b));
        // Binary One's complement(~)
        System.out.println((~5));
        // Binary left shift(<<)
        System.out.println(a<<2);
        // Binary left shift
        System.out.println(b>>1);
    }
    
}
